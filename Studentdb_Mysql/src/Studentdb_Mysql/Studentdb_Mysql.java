    private void jBResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBResultActionPerformed
     int[] marks = new int[9];
    
    // Parse scores and handle empty strings
    try {
        marks[0] = jtxtScore1.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore1.getText().trim());
        marks[1] = jtxtScore2.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore2.getText().trim());
        marks[2] = jtxtScore3.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore3.getText().trim());
        marks[3] = jtxtScore4.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore4.getText().trim());
        marks[4] = jtxtScore5.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore5.getText().trim());
        marks[5] = jtxtScore6.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore6.getText().trim());
        marks[6] = jtxtScore7.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore7.getText().trim());
        marks[7] = jtxtScore8.getText().trim().isEmpty() ? 0 : Integer.parseInt(jtxtScore8.getText().trim());

        // Calculate total score and set text field
        marks[8] = marks[0] + marks[1] + marks[2] + marks[3] + marks[4] + marks[5] + marks[6] + marks[7];
        jtxtTotalScore.setText(Integer.toString(marks[8]));
    } catch (NumberFormatException e) {
        // Handle the case where a non-integer value is entered
        JOptionPane.showMessageDialog(this, "Please enter valid scores.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Stop further execution
    }

    // Determine ranking based on total score
    if(marks[8] >= 700){
        jtxtRanking.setText("1st Class");
    }
    else if(marks[8] >= 600){
        jtxtRanking.setText("2i Class Upper");
    }
    else if(marks[8] >= 500){
        jtxtRanking.setText("2ii Class Lower");
    }
    else if(marks[8] >= 400){
        jtxtRanking.setText("3rd Class");
    }
    else if(marks[8] >= 300){
        jtxtRanking.setText("CoHE");
    }
    else if(marks[8] <= 299){
        jtxtRanking.setText("Fail");
    }

    // Set date
    Date date = jCalendar1.getDate();
    DateFormat df = new SimpleDateFormat("ss/mm/yyyy");
    String strDate = df.format(date);
    jtxtDate.setText(strDate);

    // Reset scores that exceed 100
    if (marks[0] > 100){
        jtxtScore1.setText("0");
    }
    else if (marks[1] > 100){
        jtxtScore2.setText("0");
    }
    else if (marks[2] > 100){
        jtxtScore3.setText("0");
    }
    else if (marks[3] > 100){
        jtxtScore4.setText("0");
    }
    else if (marks[4] > 100){
        jtxtScore5.setText("0");
    }
    else if (marks[5] > 100){
        jtxtScore6.setText("0");
    }
    else if (marks[6] > 100){
        jtxtScore7.setText("0");
    }
    else if (marks[7] > 100){
        jtxtScore8.setText("0");
    }
    }//GEN-LAST:event_jBResultActionPerformed
