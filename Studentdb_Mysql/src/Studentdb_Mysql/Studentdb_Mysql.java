    private void jBdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBdeleteActionPerformed
          try {
        // Establish database connection
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection sqlCon = DriverManager.getConnection(dataCon, username, password);
        
        // Prepare the delete SQL statement
        String deleteQuery = "DELETE FROM stdb WHERE StudentID = ?";
        PreparedStatement pst = sqlCon.prepareStatement(deleteQuery);
        pst.setString(1, jtxtStudentID.getText());
        
        // Execute the delete operation
        int rowsAffected = pst.executeUpdate();
        
        // Check if the delete was successful
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Student record deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No student record found with StudentID: " + studentID , "Not Found", JOptionPane.WARNING_MESSAGE);
        }
        
        // Close resources
        pst.close();
        sqlCon.close();
        
    } catch (ClassNotFoundException | SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error occurred: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jBdeleteActionPerformed

