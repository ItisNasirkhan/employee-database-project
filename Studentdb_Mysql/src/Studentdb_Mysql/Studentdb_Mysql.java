    public void uploadDB() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlCon = DriverManager.getConnection(dataCon, username, password);

            String query = "SELECT s.StudentID, s.Firstname, s.Sirname, s.DOB, s.Gender, " +
                    "p.Address, p.Mobile, p.Email, " +
                    "c.Course, c.CourseCode, " +
                    "st.HomeStudent, st.OverseasStudent, st.Accommodation, st.Exchange, st.Scholarship, " +
                    "d.BA, d.BSc, d.MA, d.MSc, d.PhD, " +
                    "r.Module1, r.Score1, r.Module2, r.Score2, r.Module3, r.Score3, r.Module4, r.Score4, " +
                    "r.Module5, r.Score5, r.Module6, r.Score6, r.Module7, r.Score7, r.Module8, r.Score8, " +
                    "r.TotalScore, r.Ranking, r.Date " +
                    "FROM studentinfo s " +
                    "JOIN personalinfo p ON s.StudentID = p.StudentID " +
                    "JOIN course c ON s.StudentID = c.StudentID " +
                    "JOIN sstatus st ON s.StudentID = st.StudentID " +
                    "JOIN sdegree d ON s.StudentID = d.StudentID " +
                    "JOIN result r ON s.StudentID = r.StudentID";

            pst = sqlCon.prepareStatement(query);
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            q = stData.getColumnCount();

            DefaultTableModel studentRecord = (DefaultTableModel) jTable1.getModel();
            studentRecord.setRowCount(0);

            while (rs.next()) {
                Vector<Object> columnData = new Vector<>();
                for (i = 1; i <= q; i++) {
                    columnData.add(rs.getObject(i));
                }
                studentRecord.addRow(columnData);
            }
        } catch (SQLException sqlEx) {
            JOptionPane.showMessageDialog(null, "SQL Error: " + sqlEx.getMessage());
            sqlEx.printStackTrace();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            ex.printStackTrace();
        } finally {
            try {
                if (rs != null) rs.close();
                if (pst != null) pst.close();
                if (sqlCon != null) sqlCon.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }




